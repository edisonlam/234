
public class History {
	
	public String symbolName;
	
	public double salePrice;
	
	public int lastSaleVolume;

	public double newPrice; 
	
	public String agentID;
	
	public History() 
	{
		
	}
	
	public String symbolName() {
		return symbolName;
	}
	
	public double salePrice() {
		return salePrice;
	}
	
	public int lastSaleVolume() {
		return lastSaleVolume;
	}
	
	public double newPrice() {
		return newPrice;
	}
	
	public String agentID() {
		return agentID;
	}
}
