
public class Transaction {

	public String symbolName;
	
	public double stockPrice;
	
	public int numberBought;
	
	public Transaction() 
	{
		
	}

	public String symbolName() {
		return symbolName;
	}

	public double stockPrice() {
		return stockPrice;
	}

	public int numberBought() {
		return numberBought;
	}
}
